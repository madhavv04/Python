def add(x,y):
    print("Addition of a and b = : ",x+y)

def sub(x,y):
    print("Subtraction of a and b = : ",x-y)

def div(x,y):
    print("Division of a and b = : ",x/y)

def mul(x,y):
    print("Multiplication of a and b = : ",x*y)

a = int(input("Enter the value for a : "))
b = int(input("Enter the value for b : "))

add(a,b)
mul(a,b)
